class Solution:
    def countCommas(self, n: int) -> int:
        if n <= 999:
            return 0
        
        if 1000 <= n <= 999999:
            return (n - 1000 + 1)
        
        if 1000000 <= n <= 999999999:
            return (n - 1000000 + 1) * 2 + (999999 - 1000 + 1)
        
        if 1000000000 <= n <= 999999999999:
            return (n - 1000000000 + 1) * 3 + (999999999 - 1000000 + 1) * 2 + (999999 - 1000 + 1)
        
        if 1000000000000 <= n <= 999999999999999:
            x = (999999999999 - 1000000000 + 1) * 3 + (999999999 - 1000000 + 1) * 2 + (999999 - 1000 + 1)
            return (n - 1000000000000 + 1) * 4 + x
        
        if 1000000000000000 <= n <= 1000000000000000:
            x = (999999999999 - 1000000000 + 1) * 3 + (999999999 - 1000000 + 1) * 2 + (999999 - 1000 + 1)
            y = (999999999999999 - 1000000000000 + 1) * 4 + x
            return (n - 1000000000000000 + 1) * 5 + y
        


        
        
        
        
            
        
        
